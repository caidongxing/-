package com.hy.vo;

import java.math.BigDecimal;
import java.util.Date;

import org.springframework.format.annotation.DateTimeFormat;

import com.hy.po.Otherinfo;

public class OtherInfo extends Otherinfo {

	private String membercardnumber;
	
	private Integer membercardtype;
	
	private String membercardname;
	
	private BigDecimal balance;
	
	private String creditcardnumber;

    private Integer creditcardtype;

    @DateTimeFormat(pattern = "yyyy-MM-dd")
    private Date validitydate;

    private BigDecimal quota;

	
    
    public String getMembercardname() {
		return membercardname;
	}

	public void setMembercardname(String membercardname) {
		this.membercardname = membercardname;
	}

	public String getMembercardnumber() {
		return membercardnumber;
	}

	public void setMembercardnumber(String membercardnumber) {
		this.membercardnumber = membercardnumber;
	}

	public Integer getMembercardtype() {
		return membercardtype;
	}

	public void setMembercardtype(Integer membercardtype) {
		this.membercardtype = membercardtype;
	}

	public BigDecimal getBalance() {
		return balance;
	}

	public void setBalance(BigDecimal balance) {
		this.balance = balance;
	}

	public String getCreditcardnumber() {
		return creditcardnumber;
	}

	public void setCreditcardnumber(String creditcardnumber) {
		this.creditcardnumber = creditcardnumber;
	}

	public Integer getCreditcardtype() {
		return creditcardtype;
	}

	public void setCreditcardtype(Integer creditcardtype) {
		this.creditcardtype = creditcardtype;
	}

	public Date getValiditydate() {
		return validitydate;
	}

	public void setValiditydate(Date validitydate) {
		this.validitydate = validitydate;
	}

	public BigDecimal getQuota() {
		return quota;
	}

	public void setQuota(BigDecimal quota) {
		this.quota = quota;
	}
    
}
