package com.hy.vo;

public class RoomReservation {

	private String teamname;
	
	private Integer signbilltemplateid;
	
	private String agreementclientname;
	
	private String reservationnumber;
	
	private Integer firstResult;
	
	private Integer pageSize;

	
	
	public String getTeamname() {
		return teamname;
	}

	public void setTeamname(String teamname) {
		this.teamname = teamname;
	}

	public Integer getSignbilltemplateid() {
		return signbilltemplateid;
	}

	public void setSignbilltemplateid(Integer signbilltemplateid) {
		this.signbilltemplateid = signbilltemplateid;
	}

	public String getAgreementclientname() {
		return agreementclientname;
	}

	public void setAgreementclientname(String agreementclientname) {
		this.agreementclientname = agreementclientname;
	}

	public String getReservationnumber() {
		return reservationnumber;
	}

	public void setReservationnumber(String reservationnumber) {
		this.reservationnumber = reservationnumber;
	}

	public Integer getFirstResult() {
		return firstResult;
	}

	public void setFirstResult(Integer firstResult) {
		this.firstResult = firstResult;
	}

	public Integer getPageSize() {
		return pageSize;
	}

	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}

}
